public class primitive_types {
    
    static int number = 52;
    static short number0 = 45;
    static long number1 = 482984;
    static byte number2 = 5;
    static float number3 = 43.5F; // F as a suffix is used to specify a float value
    static double number4 = 453.38982; // D as a suffix can be used to denote a double value
    static char letter0 = 'X'; // Char can store just one character from the unicode set
    static boolean bool0 = true;


    public static void main(String[] args) {
        System.out.println(number);
        System.out.println(number0);
        System.out.println(number1);
        System.out.println(number2);
        System.out.println(number3);
        System.out.println(number4);
        System.out.println(letter0);
        System.out.println(bool0);
    }


}
